export type MessageKey = keyof typeof messages.en;

const localeChoices: LocaleChoice[] = ['auto', 'en', 'zh-CN'];

function browserLocale(): Locale {
  const lang = typeof navigator === 'undefined' ? '' : navigator.language.toLowerCase();
  return lang.startsWith('zh') ? 'zh-CN' : 'en';
}

function savedLocaleChoice(): LocaleChoice {
  const v = typeof localStorage === 'undefined' ? null : localStorage.getItem('lp.locale');
  return localeChoices.includes(v as LocaleChoice) ? (v as LocaleChoice) : 'auto';
}

const systemLocale = ref<Locale>(browserLocale());

export const i18nState = reactive({
  localeChoice: savedLocaleChoice(),
});

export const currentLocale = computed<Locale>(() =>
  i18nState.localeChoice === 'auto' ? systemLocale.value : i18nState.localeChoice,
);

export function setLocaleChoice(choice: LocaleChoice): void {
  i18nState.localeChoice = choice;
  localStorage.setItem('lp.locale', choice);
}

export function t(key: MessageKey, vars: Record<string, string | number> = {}): string {
  const table = messages[currentLocale.value] as Record<string, string>;
  const fallback = messages.en as Record<string, string>;
  return (table[key] ?? fallback[key] ?? key).replace(/\{(\w+)\}/g, (_, name: string) =>
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : `{${name}}`,
  );
}

export function mediaTypeLabel(type: MediaType): string {
  if (type === 'continuous') return t('media.continuous');
  if (type === 'blackmark') return t('media.blackmark');
  return t('media.gap');
}

export function formatDateTime(ts: string): string {
  try {
    return new Intl.DateTimeFormat(currentLocale.value, {
      dateStyle: 'short',
      timeStyle: 'medium',
      hour12: false,
    }).format(new Date(ts));
  } catch {
    return ts;
  }
}

export function syncDocumentLocale(): void {
  if (typeof window !== 'undefined') {
    window.addEventListener('languagechange', () => {
      systemLocale.value = browserLocale();
    });
  }
  watchEffect(() => {
    document.documentElement.lang = currentLocale.value;
    document.title = t('app.title');
  });
}
